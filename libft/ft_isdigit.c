int	ft_isdigit(int c)
{
	if ((c >= '0' && c <= '9'))
		return (1);
	if (c == '+' || c == '-' || c == 32 || (c == 32 && (c == '+' || c == '-')))
		return (1);
	else
		return (0);
}
